def title_text(pdf, text):
    pdf.set_font('Times', 'B', 12)
    pdf.cell(0, LH, text, align='C', new_x="LMARGIN", new_y="NEXT")

def heading(pdf, text):
    pdf.ln(2)
    pdf.set_font('Times', 'B', 12)
    pdf.cell(0, LH, text, new_x="LMARGIN", new_y="NEXT")
    pdf.ln(2)

def subheading(pdf, text):
    pdf.ln(1)
    pdf.set_font('Times', 'B', 12)
    pdf.cell(0, LH, text, new_x="LMARGIN", new_y="NEXT")
    pdf.ln(1)

def body(pdf, text):
    pdf.set_font('Times', '', 12)
    pdf.multi_cell(0, LH, text)
    pdf.ln(0.5)

def body_bold(pdf, text):
    pdf.set_font('Times', 'B', 12)
    pdf.multi_cell(0, LH, text)
    pdf.ln(0.5)

def math(pdf, text):
    pdf.set_font('Courier', '', 11)
    pdf.multi_cell(0, LH, text)
    pdf.ln(0.5)

def math_indent(pdf, text):
    pdf.set_font('Courier', '', 11)
    lm = pdf.l_margin
    pdf.set_left_margin(lm + INDENT)
    pdf.set_x(lm + INDENT)
    pdf.multi_cell(0, LH, text)
    pdf.set_left_margin(lm)
    pdf.ln(0.3)

def step(pdf, text):
    pdf.set_font('Times', 'B', 12)
    pdf.multi_cell(0, LH, text)
    pdf.ln(0.5)

def bullet(pdf, text):
    pdf.set_font('Times', '', 12)
    lm = pdf.l_margin
    pdf.set_left_margin(lm + INDENT)
    pdf.set_x(lm + INDENT)
    pdf.multi_cell(0, LH, f'- {text}')
    pdf.set_left_margin(lm)

def tbl(pdf, text):
    pdf.set_font('Courier', '', 9)
    pdf.cell(0, LH, text, new_x="LMARGIN", new_y="NEXT")

