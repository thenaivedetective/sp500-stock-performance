math_indent(pdf, 'b = [4, 2]^T')

subheading(pdf, 'Part (ii): Construct the Simplex Tableau')
body(pdf, 'To construct the simplex tableau for the given basis, we need B^(-1), then compute B^(-1)*N, B^(-1)*b, and the reduced costs.')

step(pdf, 'Step 1: Compute B^(-1)')
math_indent(pdf, '     | 1  1 |')
math_indent(pdf, 'B =  |      |')
